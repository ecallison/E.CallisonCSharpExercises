using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ExercisesPage36CSMethods
{
    public class Calculator
    {
        public int UserNumberTimes11(int UserNumb)
        {
            return UserNumb * 11;
        }

        public int UserNumberTimesItself(int UserNumb)
        {
            return UserNumb * UserNumb;
        }

        public int UserNumberRemainder(int UserNumb)
        {
            return UserNumb % 2;
        }
    }
}
